"""**TASK 7.2a: List all individuals of "Person" with RDFLib (remember the subClasses). Return the individual URIs in a list called "individuals"**

"""

ns = Namespace("http://oeg.fi.upm.es/def/people#")
# --- 1) Recolectar Person + TODAS sus subclases (transitivas) ---
person_and_subs = set([ns.Person])
frontier = [ns.Person]

while frontier:
    current = frontier.pop()
    for sub in g.subjects(RDFS.subClassOf, current):
        if sub not in person_and_subs:
            person_and_subs.add(sub)
            frontier.append(sub)

# --- 2) Obtener individuos tipados en cualquiera de esas clases ---
ind_set = set()
for cls in person_and_subs:
    for s in g.subjects(RDF.type, cls):
        ind_set.add(s)

# --- 3) Lista final ordenada (por string) ---
individuals = sorted(ind_set, key=str)
# visualize results
for i in individuals:
  print(i)

# validation. Do not remove
report.validate_07_02a(individuals)

