"""**TASK 7.1a: For all classes, list each classURI. If the class belogs to another class, then list its superclass.**
**Do the exercise in RDFLib returning a list of Tuples: (class, superclass) called "result". If a class does not have a super class, then return None as the superclass**
"""

# TO DO
# --- Recolectar todas las clases ---
classes = set()

# 1) Clases declaradas expl√≠citamente
for c in g.subjects(RDF.type, RDFS.Class):
    classes.add(c)

# 2) Clases que aparecen en axiomas de subClassOf (como subclase u superclase)
for s, o in g.subject_objects(RDFS.subClassOf):
    classes.add(s)  # subclase
    classes.add(o)  # superclase

# --- Construir (class, superclass) ---
result = []
for c in classes:
    superclasses = list(g.objects(c, RDFS.subClassOf))
    if superclasses:
        for sup in superclasses:
            result.append((c, sup))
    else:
        result.append((c, None))

# Visualize the results
for r in result:
  print(r)

## Validation: Do not remove
report.validate_07_1a(result)

"""**TASK 7.1b: Repeat the same exercise in SPARQL, returning the variables ?c (class) and ?sc (superclass)**"""

query =  """SELECT DISTINCT ?c ?sc
WHERE {
  {
    { ?c a rdfs:Class }
    UNION
    { ?c rdfs:subClassOf ?_any }
    UNION
    { ?_any rdfs:subClassOf ?c }
  }
  OPTIONAL { ?c rdfs:subClassOf ?sc }
}
ORDER BY ?c ?sc
"""

for r in g.query(query):
  print(r.c, r.sc)

